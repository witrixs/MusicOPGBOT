const { ApplicationCommandOptionType, EmbedBuilder } = require('discord.js'); // –ò–º–ø–æ—Ä—Ç–∏—Ä—É–µ–º –Ω–µ–æ–±—Ö–æ–¥–∏–º—ã–µ –∫–ª–∞—Å—Å—ã
const { QueryType, useMainPlayer, useQueue } = require('discord-player'); // –ò–º–ø–æ—Ä—Ç–∏—Ä—É–µ–º —Ñ—É–Ω–∫—Ü–∏–∏ –¥–ª—è —Ä–∞–±–æ—Ç—ã —Å –º—É–∑—ã–∫–∞–ª—å–Ω—ã–º –ø–ª–µ–µ—Ä–æ–º
const { Translate } = require('../../process_tools'); // –ò–º–ø–æ—Ä—Ç–∏—Ä—É–µ–º —Ñ—É–Ω–∫—Ü–∏—é –ø–µ—Ä–µ–≤–æ–¥–∞ —Ç–µ–∫—Å—Ç–æ–≤

module.exports = {
    name: 'playnext', // –ò–º—è –∫–æ–º–∞–Ω–¥—ã
    description: ("–í–æ—Å–ø—Ä–æ–∏–∑–≤–µ—Å—Ç–∏ –ø–µ—Å–Ω—é —Å—Ä–∞–∑—É –ø–æ—Å–ª–µ —ç—Ç–æ–π"), // –û–ø–∏—Å–∞–Ω–∏–µ –∫–æ–º–∞–Ω–¥—ã
    voiceChannel: true, // –£–∫–∞–∑—ã–≤–∞–µ–º, —á—Ç–æ –∫–æ–º–∞–Ω–¥–∞ –º–æ–∂–µ—Ç –±—ã—Ç—å –≤—ã–∑–≤–∞–Ω–∞ —Ç–æ–ª—å–∫–æ –≤ –≥–æ–ª–æ—Å–æ–≤–æ–º –∫–∞–Ω–∞–ª–µ
    options: [
        {
            name: 'song', // –ù–∞–∑–≤–∞–Ω–∏–µ –æ–ø—Ü–∏–∏
            description: ('–ü–µ—Å–Ω—è, –∫–æ—Ç–æ—Ä—É—é –≤—ã —Ö–æ—Ç–∏—Ç–µ –≤–æ—Å–ø—Ä–æ–∏–∑–≤–µ—Å—Ç–∏ —Å–ª–µ–¥—É—é—â–µ–π'), // –û–ø–∏—Å–∞–Ω–∏–µ –æ–ø—Ü–∏–∏
            type: ApplicationCommandOptionType.String, // –¢–∏–ø –æ–ø—Ü–∏–∏ (—Å—Ç—Ä–æ–∫–∞)
            required: true, // –û–ø—Ü–∏—è –æ–±—è–∑–∞—Ç–µ–ª—å–Ω–∞—è
        }
    ],

    async execute({ inter }) {
        const player = useMainPlayer(); // –ü–æ–ª—É—á–∞–µ–º –æ—Å–Ω–æ–≤–Ω–æ–π –º—É–∑—ã–∫–∞–ª—å–Ω—ã–π –ø–ª–µ–µ—Ä
        const queue = useQueue(inter.guild); // –ü–æ–ª—É—á–∞–µ–º –æ—á–µ—Ä–µ–¥—å –¥–ª—è —Ç–µ–∫—É—â–µ–≥–æ —Å–µ—Ä–≤–µ—Ä–∞

        // –ü—Ä–æ–≤–µ—Ä—è–µ–º, –≤–æ—Å–ø—Ä–æ–∏–∑–≤–æ–¥–∏—Ç—Å—è –ª–∏ –º—É–∑—ã–∫–∞
        if (!queue?.isPlaying()) 
            return inter.editReply({ content: await Translate(`–°–µ–π—á–∞—Å –Ω–∏—á–µ–≥–æ –Ω–µ –≤–æ—Å–ø—Ä–æ–∏–∑–≤–æ–¥–∏—Ç—Å—è <${inter.member}>... –ø–æ–ø—Ä–æ–±—É–π—Ç–µ —Å–Ω–æ–≤–∞ ? <‚ùå>`) });

        const song = inter.options.getString('song'); // –ü–æ–ª—É—á–∞–µ–º –Ω–∞–∑–≤–∞–Ω–∏–µ –ø–µ—Å–Ω–∏ –∏–∑ –æ–ø—Ü–∏–π
        const res = await player.search(song, { // –ò—â–µ–º —Ç—Ä–µ–∫ –ø–æ –Ω–∞–∑–≤–∞–Ω–∏—é
            requestedBy: inter.member, // –£–∫–∞–∑—ã–≤–∞–µ–º, –∫—Ç–æ –∑–∞–ø—Ä–æ—Å–∏–ª —Ç—Ä–µ–∫
            searchEngine: QueryType.AUTO // –ê–≤—Ç–æ–º–∞—Ç–∏—á–µ—Å–∫–∏–π –≤—ã–±–æ—Ä —Ç–∏–ø–∞ –ø–æ–∏—Å–∫–∞
        });

        // –ü—Ä–æ–≤–µ—Ä—è–µ–º, –±—ã–ª–∏ –ª–∏ –Ω–∞–π–¥–µ–Ω—ã —Ä–µ–∑—É–ª—å—Ç–∞—Ç—ã
        if (!res?.tracks.length) 
            return inter.editReply({ content: await Translate(`–ù–µ –Ω–∞–π–¥–µ–Ω–æ —Ä–µ–∑—É–ª—å—Ç–∞—Ç–æ–≤ <${inter.member}>... –ø–æ–ø—Ä–æ–±—É–π—Ç–µ —Å–Ω–æ–≤–∞ ? <‚ùå>`) });

        // –ü—Ä–æ–≤–µ—Ä—è–µ–º, —è–≤–ª—è–µ—Ç—Å—è –ª–∏ —Ä–µ–∑—É–ª—å—Ç–∞—Ç –ø–ª–µ–π–ª–∏—Å—Ç–æ–º
        if (res.playlist) 
            return inter.editReply({ content: await Translate(`–≠—Ç–∞ –∫–æ–º–∞–Ω–¥–∞ –Ω–µ –ø–æ–¥–¥–µ—Ä–∂–∏–≤–∞–µ—Ç –ø–ª–µ–π–ª–∏—Å—Ç—ã <${inter.member}>... –ø–æ–ø—Ä–æ–±—É–π—Ç–µ —Å–Ω–æ–≤–∞ ? <‚ùå>`) });

        // –í—Å—Ç–∞–≤–ª—è–µ–º —Ç—Ä–µ–∫ –≤ –æ—á–µ—Ä–µ–¥—å –Ω–∞ –ø–µ—Ä–≤–æ–µ –º–µ—Å—Ç–æ
        queue.insertTrack(res.tracks[0], 0);

        // –°–æ–∑–¥–∞–µ–º embed-—Å–æ–æ–±—â–µ–Ω–∏–µ —Å –ø–æ–¥—Ç–≤–µ—Ä–∂–¥–µ–Ω–∏–µ–º
        const playNextEmbed = new EmbedBuilder()
            .setAuthor({ name: await Translate(`–¢—Ä–µ–∫ –¥–æ–±–∞–≤–ª–µ–Ω –≤ –æ—á–µ—Ä–µ–¥—å... –æ–Ω –±—É–¥–µ—Ç –≤–æ—Å–ø—Ä–æ–∏–∑–≤–µ–¥–µ–Ω —Å–ª–µ–¥—É—é—â–∏–º <üéß>`) })
            .setColor('#2f3136'); // –£—Å—Ç–∞–Ω–∞–≤–ª–∏–≤–∞–µ–º —Ü–≤–µ—Ç —Å–æ–æ–±—â–µ–Ω–∏—è

        // –û—Ç–ø—Ä–∞–≤–ª—è–µ–º –æ—Ç–≤–µ—Ç —Å embed-—Å–æ–æ–±—â–µ–Ω–∏–µ–º
        await inter.editReply({ embeds: [playNextEmbed] });
    }
}
